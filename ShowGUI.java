    private void tabelaPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_tabelaPropertyChange
        DefaultTableModel tblModel = (DefaultTableModel) tabela.getModel();
        for (int i = 0; i < tblModel.getRowCount(); i++) {
            String pass = tblModel.getValueAt(i, 2).toString();
            System.out.println("ShowGUI -> tabelaUpdate : " + pass);
            String strength = StrongPassword.passwordCheck(pass);
            tblModel.setValueAt(strength, i, 3);
        }
        tabela.repaint();
        //JOptionPane.showMessageDialog(null, "Updated", "Notification", 1);
    }//GEN-LAST:event_tabelaPropertyChange

