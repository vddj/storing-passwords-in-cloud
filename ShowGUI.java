    private void passButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passButtonActionPerformed
        
        drv.updatePassword(new String(this.sifra.getPassword()));
        if (drv.isGoodPassword()) {
            String correctPass = JSONfiles.getPassForThis();
            String insertedPass = new String(this.sifra.getPassword());
            System.out.println("ShowGUI -> passButton -> insertPass/correctPass: " + insertedPass + "/" + correctPass);
            parentPanel.removeAll();
            parentPanel.add(panelLoggedIn);
            parentPanel.repaint();
            parentPanel.revalidate();
            this.repaintTable();
        } else {
            JOptionPane.showMessageDialog(null, "Your password is not correct", "Notification", 1);
        }
    }//GEN-LAST:event_passButtonActionPerformed

    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String pass1 = new String(this.jPasswordField1.getPassword());
        String pass2 = new String(this.jPasswordField2.getPassword());
        if(pass1.equals(pass2)){
            drv.setPassword(pass1);
            JOptionPane.showMessageDialog(null, "Your password is in use", "Notification", 1);
            parentPanel.removeAll();
            parentPanel.add(panelLoggedIn);
            parentPanel.repaint();
            parentPanel.revalidate();
            this.repaintTable();
        }
        else {
            JOptionPane.showMessageDialog(null, "Passwords are not equal", "Notification", 1);
        }
        
    }//GEN-LAST:event_submitButtonActionPerformed

