    private void repaintTable(){
        JSONArray lista = JSONfiles.readFromJSONfile();
        if(lista == null || lista.isEmpty()) return;
        DefaultTableModel tblModel = (DefaultTableModel) tabela.getModel();
        for (Object object : lista) {
            JSONObject obj1 = (JSONObject) object;
            String niz[] = JSONfiles.parseObject(obj1);
            tblModel.addRow(niz);
        }
    }
    
    private void saveAllPassToCloud(boolean b) {
        DefaultTableModel tblModel = (DefaultTableModel) tabela.getModel();
        Object objekti[] = tblModel.getDataVector().toArray();
        System.out.println(tblModel.getDataVector().toString());
        JSONfiles.rewriteJSONfile(objekti);
        try {
            drv.updateFile();
        } catch (IOException ex) {
            Logger.getLogger(ShowGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(b) JOptionPane.showMessageDialog(null, "Your passwords are updated", "Notification", 1);
    }
    
